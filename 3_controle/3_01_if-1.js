// Uso de 'estruturas condicionais' como o "IF":

if (true) //Com o uso do "if" é importante não usar o ";" no final da linha!
    console.log("Vai ser executado!");

if (false) // Uso de valor literal. // Como já é FALSE, o JS não irá ler o código.
    console.log("Não vai ser executado!"); //Perceba que a tipografia está apagada / opaca, reafirmando que não irá ler o código

console.log("Fim.");