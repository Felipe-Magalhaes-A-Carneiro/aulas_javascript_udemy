// Operadores relacionais:

/* 

> : ...é maior que...
< : ...é menor que...
>= : ...é maior ou igual a...
<= : ...é menor ou igual a...
== : ...é igual a... (igualdade)
=== : ... é estrito a... (comparação de igualdade estrita)
!= : ...é diferente... (diferença)
!== : ...é estritamente diferente... (diferença estrita)

*/

// Na prática:

  console.log(7 > 9);
  console.log(7 < 8);
  console.log(8 >= 12);
  console.log(8 <= 12);
  console.log(9 == 3);
  console.log(9 === 9);
  console.log(9 != 5);
  console.log(9 !== 9);

  // Utilizando variáveis:
console.log("Utilizando operadores relacionais com variáveis: ")

